package portLogic;

public class LabelData {
    private final String address;
    private final String data;

    public LabelData(String address, String data) {
        this.address = address;
        this.data = data;
    }

    public String getAddress() {
        return address;
    }

    public String getData() {
        return data;
    }
    
    
    
}
